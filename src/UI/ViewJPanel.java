        HashMap diplayAbnormalBPByCommunity =  system.displayPatientsByAbnormalBP();
        HashMap diplayAbnormalHRByCommunity =  system.displayPatientsByAbnormalHR();
        
        DefaultTableModel tableModel = (DefaultTableModel) tblAbnormal.getModel();
        tableModel.setRowCount(0);

        //loop to display the abnormal heart rate patient 
        diplayAbnormalHRByCommunity.forEach((key, value) -> {
            Object [] tableRow = new Object[10];
            tableRow[0] = key;
            tableRow[1] = value;
            tableRow[2] = "HeartRate";
            tableModel.addRow(tableRow);
        });
        
        //loop to display the abnormal blood pressure patient 
        diplayAbnormalBPByCommunity.forEach((key, value) -> {
            Object [] tableRow = new Object[10];
            tableRow[0] = key;
            tableRow[1] = value;
            tableRow[2] = "BP";
            tableModel.addRow(tableRow);
        });
        
