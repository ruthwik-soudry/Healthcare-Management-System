        private void addMockData() {
        
        //adding patients records into the table
        DefaultTableModel tableModel = (DefaultTableModel) tblModel.getModel();
        tableModel.setRowCount(0);
        
        //loop to display the patient information
        system.getPatientDirectory().forEach(patient -> {
            Object [] tableRow = new Object[10];
            tableRow[0] = patient.getPersonId();
            tableRow[1] = patient.getFirstName()+" "+patient.getLastName();
            tableRow[2] = patient.getDateOfBirth().toString();
            tableRow[3] = patient.getAgeGroup().toString();
            tableRow[5] = patient.getResidence().getHouseNumber();
            tableRow[6] = patient.getResidence().getCommunityName();
            tableRow[4] = patient.getResidence().getCity();
            tableModel.addRow(tableRow);
            
        });        

    }

